package tiposprimitivos;

public class tiposprimitivos4 {

    public static void main(String[] args){

        /* Os ultimos 2 tipos primitivos são double e float 
            que servem para numeros com ponto flutuante */ 

        // double é mais preciso com as casa decimais
        double altura = 1.62;

        // float é menos preciso com as casas decimais. OBS: Sempre coloque um f no final de um valor float
        float preco = 2.50f;

        System.out.println(altura);
        System.out.println(preco);

    }
    
}
