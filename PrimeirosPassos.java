public class PrimeirosPassos {

    public static void main(String[] args){
        
        // Chamando as funções
        somaDeNumeros();
        subtracaoDeNumeros();
        multiplicacaoDeNumeros();
        divisaoDeNumeros();

    }

   static void somaDeNumeros(){

        // Imprimindo no terminal a soma de dois números
        System.out.println(2 + 2);

    }

    static void subtracaoDeNumeros(){

        // Imprimindo no terminal a subtração de dois números
        System.out.println(2 - 2);

    }

    static void multiplicacaoDeNumeros(){

        // Imprimindo no terminal a multiplicação de dois números
        System.out.println(2 * 2);

    }

    static void divisaoDeNumeros(){

        // Imprimindo no terminal a divisão de dois números
        System.out.println(2 / 2);

    }
    
}
