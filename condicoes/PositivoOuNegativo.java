package condicoes;

public class PositivoOuNegativo {

    public static void main(String[] args){

        int numero = 10;

        if (numero > 0){

            System.out.println("Este número é positivo!");

        }
        else if(numero < 0){

            System.out.println("Este número é negativo!");

        }
        else{

            System.out.println("Este número é igual a 0!");

        }

    }
    
}
